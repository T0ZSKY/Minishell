#include "../../../includes/header/minishell.h"

void ft_echo(char **tab)
{
	int i;

	i = 1;
	while (tab[i])
	{
		printf("%s", tab[i]);
		i++;
		if (tab[i])
			printf(" ");
	}
	printf("\n");
}
